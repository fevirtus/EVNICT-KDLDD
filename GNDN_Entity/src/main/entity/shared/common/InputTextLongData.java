package main.entity.shared.common;

import java.io.Serializable;
import java.util.List;

/**
 *
 * @author khiemvk
 */

public class InputTextLongData implements Serializable{
    public String tableName;//Tên bảng
    public List<String> keyColums;//Các cột khóa
    public List<String> keyValues;//Các cột giá trị khóa
    public String colText;//Cột text dài
    public String longText;//Giá trị cột text dài
    public String colUserIDUpdate;//Cột user cập nhật
    public String colDateUpdate;//Cột ngày cập nhật
    public String userid;

    public String error;

    public InputTextLongData(){

    }

    public String getColDateUpdate() {
        return colDateUpdate;
    }

    public void setColDateUpdate(String colDateUpdate) {
        this.colDateUpdate = colDateUpdate;
    }

    public String getColText() {
        return colText;
    }

    public void setColText(String colText) {
        this.colText = colText;
    }

    public String getColUserIDUpdate() {
        return colUserIDUpdate;
    }

    public void setColUserIDUpdate(String colUserIDUpdate) {
        this.colUserIDUpdate = colUserIDUpdate;
    }

    public List<String> getKeyColums() {
        return keyColums;
    }

    public void setKeyColums(List<String> keyColums) {
        this.keyColums = keyColums;
    }

    public List<String> getKeyValues() {
        return keyValues;
    }

    public void setKeyValues(List<String> keyValues) {
        this.keyValues = keyValues;
    }

    public String getLongText() {
        return longText;
    }

    public void setLongText(String longText) {
        this.longText = longText;
    }

    public String getTableName() {
        return tableName;
    }

    public void setTableName(String tableName) {
        this.tableName = tableName;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getError() {
        return error;
    }

    public void setError(String error) {
        this.error = error;
    }

    @Override
    public String toString() {
        return "tableName=" + tableName + "]";
    }
}
